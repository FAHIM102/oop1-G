
 
package beginerjava;


public class AssignmentOperator {
    public static void main(String[] args) {
        int x=56,y=30;
        
        x+=y;//x=x+y
        System.out.println("x="+x);
        x-=y;//x=x-y
        System.out.println("x="+x);
        x*=y;//x=x*y
        System.out.println("x="+x);
        x/=y;//x=x/y
        System.out.println("x="+x);
        x%=y;//x=x%y
        System.out.println("x="+x);
    }
  
}
