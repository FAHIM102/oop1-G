
package beginerjava;


public class IncrementDecrement {
    public static void main(String[] args) {
        int x=10;
        int y;
        
        y = ++x;//11
        System.out.println("y="+y);
        y = x++;//11
        System.out.println("y="+y);
        y = --x;//11
        System.out.println("y="+y);
        y = x--;//11
        System.out.println("y="+y);
        
    }
    
}
